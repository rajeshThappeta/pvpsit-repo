//arithmetic operators( + - * / %)
let a=10;
let b=5;
console.log("quotient is ",a/b)
console.log("remainder is ",a%b)

//assignment operator (=)

//comparision( ==, ===, <,<=,>,>/,!=)
let c=100;
let d="100";
//equal to (==)(it compares only values ,not datatypes)
console.log(c==d)
//strict equal to(===)(it compares values & datatypes also)
console.log(c===d)

//logical operators(&&,||) (compare conditions)
console.log(a==10 && b==5 && c==100)

//not logical operator (revert the value)
console.log(!a==10)

//conditional operator( ? :)(ternary)
//condition ? expression-1 : expression-2
let result= a>b ? a:b;
console.log(result)




